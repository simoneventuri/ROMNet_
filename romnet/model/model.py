import abc


#=======================================================================================================================================
class Model(abc.ABC):


    #===================================================================================================================================
    @abc.abstractmethod
    def train(self, InputData):
        pass
    #===================================================================================================================================



    #===================================================================================================================================
    @abc.abstractmethod
    def load_params(self, InputData):
        pass
    #===================================================================================================================================



    #===================================================================================================================================
    @abc.abstractmethod
    def predict(self, xData):
        pass
    #===================================================================================================================================